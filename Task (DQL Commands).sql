--01
SELECT 
    ENAME, 
    SAL
FROM 
    EMP
WHERE 
    SAL > 2000;

--02
SELECT 
    ENAME, 
    DEPTNO
FROM 
    EMP
WHERE 
    EMPNO = 7902;

--03
SELECT 
    ENAME, 
    SAL
FROM 
    EMP
WHERE 
    SAL NOT BETWEEN 2000 AND 5000;

--04
SELECT 
    ENAME, 
    JOB, 
    HIREDATE
FROM 
    EMP
WHERE 
    HIREDATE BETWEEN TO_DATE('20-FEB-1981', 'DD-MON-YYYY') 
    AND TO_DATE('01-MAY-1981', 'DD-MON-YYYY')
ORDER BY 
    HIREDATE ASC;

--05
SELECT 
    ENAME, 
    DEPTNO
FROM 
    EMP
WHERE 
    DEPTNO IN (10, 30)
ORDER BY 
    ENAME ASC;

--06
SELECT 
    ENAME AS "Employee", 
    SAL AS "Monthly Salary"
FROM 
    EMP
WHERE 
    SAL BETWEEN 2000 AND 3000
    AND DEPTNO IN (10, 30);

--07
SELECT 
    ENAME, 
    HIREDATE
FROM 
    EMP
WHERE 
    EXTRACT(YEAR FROM HIREDATE) = 1981;
